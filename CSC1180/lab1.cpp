#include <iostream>
using namespace std;

int main()
{
  // variable declaration
  int number1{0};
  int number2{0};
  int sum{0};
  int diff{0};
  int prod{0};
  int quot{0};
    
  // ask for user input
  cout << "Enter first integer: ";
  cin >> number1;

  cout << "Enter second integer: ";
  cin >> number2;  

  // calculate 
  sum = number1 + number2;
  diff = number1 - number2;
  prod = number1 * number2;
  if (number2 != 0)
    {
      quot = number1 / number2;
    }
  
  // output results

    cout << number1<< " + " << number2 <<" = " << sum << endl;
    cout << number1<< " - " << number2 <<" = " << diff << endl;
    cout << number1<< " * " << number2 <<" = " << prod << endl;
    if (number2 == 0)
      {
	cout << "number2 cannot be 0\n";
      }
    else
      {
	cout << number1<< " / " << number2 <<" = " << quot << endl;    
      }
  return 0;
}
